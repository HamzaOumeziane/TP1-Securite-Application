drop table EVALUATION;
drop table COURS;
drop table GROUPE;
drop table SEMESTRE;
drop table ETUDIANT;


CREATE TABLE ETUDIANT(
DA VARCHAR(10) PRIMARY KEY,
ENOM VARCHAR(25),
ADRESSE VARCHAR(25),
TELEPHONE NUMBER(10),
GENRE CHAR(1));

CREATE TABLE SEMESTRE(
ID VARCHAR(5) PRIMARY KEY,
SEM NUMBER(2),
SEC CHAR(1));

CREATE TABLE GROUPE(
DA VARCHAR(10),
ID VARCHAR(5),
PRIMARY KEY(DA,ID),
FOREIGN KEY(DA) REFERENCES ETUDIANT(DA),
FOREIGN KEY(ID) REFERENCES SEMESTRE(ID));

CREATE TABLE COURS(
CODE VARCHAR(8),
TITRE VARCHAR(20),
SEM NUMBER(2),
CREDITS NUMBER(2),
PRIMARY KEY(CODE));

CREATE TABLE EVALUATION(
DA VARCHAR(10),
CODE VARCHAR(8),
ID VARCHAR(5),
TEST1 NUMBER(2),
TEST2 NUMBER(2),
TEST3 NUMBER(2),
FINAL NUMBER(2),
PRIMARY KEY(CODE,DA,ID),
FOREIGN KEY(DA) REFERENCES ETUDIANT(DA),
FOREIGN KEY(CODE) REFERENCES COURS(CODE),
FOREIGN KEY(ID) REFERENCES SEMESTRE(ID));


